// write an examplee on function declaration, function expression and arrow function


// Function Declaration

function add(a, b) {
    const add = a + b;
    console.log(add);
}

add(2, 3);

// Function Expression

const add = function(a, b) {

    const add = a + b;
    console.log(add);
}

add(2, 3);

// Arrow Function

const add = (a, b) => {

    const add = a + b;
    console.log(add);
}   

add(2, 3);

